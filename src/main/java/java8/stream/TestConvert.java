package java8.stream;

public class TestConvert {
    public static void main(String[] args) {
        int a = 0;
        int b = 1;
        System.out.println(a ^ b);
        System.out.println(a & b);
        System.out.println(a | b);

        int c = 1;
        int d = 1;
        System.out.println(c ^ d);
        System.out.println(c ^ d);
        System.out.println(c ^ d);
    }
}
